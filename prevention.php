      <span class="BulletTextItalic">Prevention is better than a cure </span><br />
    <p> Regular screenings for cancer, diabetes, heart disease and other chronic conditions constitute the core of Dr. Krishnan&#39;s preventive medicine. By uncovering disease in its earliest stage, much can be done to halt its progression. The right treatment can bring longer-term good health to patients.</p>
    <p>Dr. Krishnan&rsquo;s office uses advanced ultrasound technology to screen for plaque buildup in the carotid artery (in the neck) and for abnormal ballooning of the aortic artery in the abdomen. These conditions can lead to either stroke or an aneurysm rupture, and often go undetected until an episode occurs. </p>
    <p>Painless, non-invasive testing goes a long way toward identifying potential stroke and aneurysm rupture. Test results are used to plan treatment for any detected plaque buildup in the carotid artery or for the potentially life-threatening risk of an aortic aneurysm. The use of these tests underscores Dr. Krishnan&rsquo;s emphasis on prevention and the importance of developing a personal wellness plan.</p>
